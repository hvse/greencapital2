		<div>By means of Maquila, investors can introduce to the country; goods, products or services in order to be assembled, repaired, improved, or processed for their subsequent exportation, once the value added or “Paraguayan element” is incorporated.<br>
		Paraguayan legislation does not impose restrictions regarding the types of products or services included in the maquila industry. The national policy of maquila activity in Paraguay is regulated and monitored by the National Council of the Maquila Export Industry (CNIME for its acronym in Spanish). Those who can benefit from this law are individuals or corporations, domestic or foreign, domiciled in the country.<a href="http://www.maquila.gov.py">www.maquila.gov.py</a>)</div>
		<div><br>
		<strong>The most important benefits are:</strong></div>
		<div>• 1% Maquila Unique Tax (over national value added or turnover, whichever is greater)</div>
		<div>• Cancellation of fees and taxes to importation of raw materials and consumables, and capital assets.</div>
		<div>• Up to 10% of the import can be destined to the internal market, since the second year, prior nationalization of consumables which were imported with suspension.</div>
		<div>• Recuperation of the value-added tax through Tax Credit.</div>
		<div>• The possibility to develop Mixed Maquila</div>
		<div><br>
		<strong>Types of Maquila</strong>
		&nbsp;</div>
		<div>Paraguayan legislation allows the following categories of maquila:<br>
		&nbsp;</div>
		<div>• Idle Capacity Maquila: this category is oriented to those companies with the possibility to implement productive processes of maquila with remains, excess or idle factors than the ones used normally.<br>
		&nbsp;</div>
		<div>• Sub-Maquila: maquila companies authorized to make contracts of sub-maquila, when they involve a complement to the productive process of the activity object of the program, to be subsequently integrated to the maquila company who originally hired the service and that will finish the product for the subsequent exportation. Companies exclusively dedicated to sub-maquila may be established.<br>
		&nbsp;</div>
		<div>• Maquila of Intangible Service: category aimed at giving an intellectual added value or another sort similar to intangible goods imported temporarily by any other electronic mean. It includes the import of intangible goods or intangibles for its transformation with national added value and subsequent exportation by electronic means (for tangible and intangible goods) or tangibles (only for intangible goods).<br>
		&nbsp;</div>
		<div>• Maquila with Shelter Program: category consisting of approval in favor of a company, of a program that will serve to make export projects from companies that will facilitate technology and production materials, without running them directly. For the fulfillment of this program, foreign companies must build a branch in Paraguay (“Twin Plant”) or set up a consortium with a Paraguayan company, in this last case, it is not necessary for it to be domiciled in Paraguay. <br>
